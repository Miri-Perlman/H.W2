# יש להריץ pip install pandas openpyxl לפני השימוש
import pandas as pd
print("הקובץ רץ!")

# קריאת הקובץ (עדיף להשתמש ב-r לפני המחרוזת לנתיב)
file_path = r'H:\כלים לבינה מלאכותית\grades.xlsx'
df = pd.read_excel(file_path)
print(df.head())

# נניח ששם העמודה של השמות הוא 'שמות' ושל הציונים הוא 'ציונים'
# אם יש כמה עמודות ציונים, עדכן בהתאם!
df['ממוצע'] = df['ציונים']  # אם יש רק עמודת ציונים אחת

print("ממוצע ציונים לכל בת:")
print(df[['שמות', 'ממוצע']])

print("\nממוצע ציונים לכל מבחן:")
print(df['ציונים'].mean())

print("\nמספר בנות שעברו כל מבחן (ציון >= 50):")
print((df['ציונים'] >= 50).sum())

print("\nבנות שעולות לשנה הבאה (ממוצע > 50):")
print(df[df['ממוצע'] > 50]['שמות'])